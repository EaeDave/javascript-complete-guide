let myVariable;
console.log(typeof myVariable); // returns the type of the value (undefined in this case).
console.log(typeof 'David'); // string.
console.log(typeof 123); // number.
console.log(typeof true); // boolean.
console.log(typeof [1, 2, 3]); // object (array is an object type).
console.log(typeof { myName: 'David', myAge: 23 }); // object.
console.log(typeof null); // object.
console.log(typeof NaN); // number.
