// boolean operators
console.log(2 == 2); // true
console.log(2 == '2'); // true
console.log(2 === '2'); // false (strict equal, needs to be the same type).

const a = 10;
const b = 12;

console.log(a != b); //true
console.log(a > b); // false
console.log(a < b); // true
const verdadeiro = true;
console.log(!verdadeiro); // false (! negate the value).
